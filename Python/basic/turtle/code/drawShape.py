import turtle

turtle.setup(width=1000, height=300)
t = turtle.Turtle()

t.penup()
t.goto(-400,0)
t.color("red")
t.pendown()
t.circle(50)
t.penup()
t.goto(-400, -50)
t.write("원", font=(15))

t.penup()
t.goto(-300,0)
t.color("orange")
t.pendown()
t.circle(50, steps=3)
t.penup()
t.goto(-300, -50)
t.write("삼각형", font=(15))

t.penup()
t.goto(-200,0)
t.color("green")
t.pendown()
t.circle(50, steps=4)
t.penup()
t.goto(-200, -50)
t.write("사각형", font=(15))

t.penup()
t.goto(-100,0)
t.color("blue")
t.pendown()
t.circle(50, steps=5)
t.penup()
t.goto(-100, -50)
t.write("오각형", font=(15))

t.penup()
t.goto(0,0)
t.color("blue")
t.pendown()
t.circle(50, steps=6)
t.penup()
t.goto(0, -50)
t.write("육각형", font=(15))

